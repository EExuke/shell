#-----------------------------------------------------------
#猜数字游戏
#脚本生成一个 100 以内的随机数字,提示用户才数字,根据用户的输入,提示用户是否猜对,
#猜大或猜小继续循环猜,猜对跳出循环。
#-----------------------------------------------------------
#                  MAIN PROCESS
#-----------------------------------------------------------
num=$[RANDOM % 100 + 1]

while :
do
	read -p "计算机生成了一个1-100的随机数，你猜：" cat
	if [[ $cat -eq $num ]]; then
		echo "猜对了"
		break
	elif [[ $cat -le $num ]]; then
		echo "小了"
	else
		echo "大了"
	fi
done




